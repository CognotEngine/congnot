// 当前语言
export let currentLanguage = 'en-US';

// 设置语言
export const setLanguage = (language: string) => {
  currentLanguage = language;
  // 可以在这里添加语言切换的其他逻辑，比如保存到localStorage
  localStorage.setItem('language', language);
};

// 获取翻译文本
export const t = (key: string, params?: { [key: string]: string | number }): string => {
  let text = resources[currentLanguage]?.[key] || resources['en-US']?.[key] || key;
  
  // 替换参数
  if (params) {
    Object.entries(params).forEach(([paramKey, paramValue]) => {
      text = text.replace(`{{${paramKey}}}`, String(paramValue));
    });
  }
  
  return text;
};

// 初始化语言
export const initLanguage = () => {
  // 从localStorage获取语言设置，如果没有则使用浏览器语言
  const savedLanguage = localStorage.getItem('language');
  const browserLanguage = navigator.language || 'en-US';
  
  if (savedLanguage && resources[savedLanguage]) {
    currentLanguage = savedLanguage;
  } else if (resources[browserLanguage]) {
    currentLanguage = browserLanguage;
  } else if (resources[browserLanguage.split('-')[0]]) {
    // 尝试匹配语言前缀，比如'en'匹配'en-US'
    currentLanguage = browserLanguage.split('-')[0];
  }
};